package com.example.demo;

public class Alien {
    private int aid;
    private String aname;
    private String lang;

    public String getLang() {
    	return this.lang;
    }
    public void setLang(String lang) {
    	this.lang = lang;
    }



    public String getAname() {
    	return this.aname;
    }
    public void setAname(String aname) {
    	this.aname = aname;
    }

    public int getAid() {
        return this.aid;
    }
    public void setAid(int aid) {
        this.aid = aid;
    }


    
}
